def atomic_scattering_factor(element: str, s: float) -> float:
    """
    计算原子散射因子 f(s)
    s = sin(theta) / lambda  (单位: 1/埃)
    """
    element = element.strip()
    
    base_element = ''.join(c for c in element if c.isalpha())
    
    if base_element not in ATOMIC_SCATTERING_FACTORS:
        base_element = _find_closest_element(base_element)
    
    data = ATOMIC_SCATTERING_FACTORS.get(base_element)
    if data is None:
        return 6.0
    
    a = data["a"]
    b = data["b"]
    c = data.get("c", 0.0)
    if isinstance(c, str):
        c = 0.0
    
    s2 = s * s
    f = c
    for ai, bi in zip(a, b):
        f += ai * np.exp(-bi * s2)
    
    return f


def _find_closest_element(element: str) -> str:
    """查找最接近的元素符号"""
    element = element.capitalize()
    if element in ATOMIC_SCATTERING_FACTORS:
        return element
    
    for known in ATOMIC_SCATTERING_FACTORS.keys():
        if known.startswith(element[0]):
            return known
    
    return "C"


def atomic_form_factor_array(elements: List[str], s_values: np.ndarray) -> np.ndarray:
    """批量计算多个元素在多个s值下的原子散射因子"""
    n_elem = len(elements)
    n_s = len(s_values)
    f = np.zeros((n_elem, n_s))
    
    for i, elem in enumerate(elements):
        for j, s in enumerate(s_values):
            f[i, j] = atomic_scattering_factor(elem, s)
    
    return f


def structure_factor(h: int, k: int, l: int, atoms: List, d: float, wavelength: float) -> complex:
    """
    计算结构因子 F_hkl
    
    参数:
        h, k, l: 衍射指标
        atoms: 原子列表 (Atom对象)
        d: 面间距
        wavelength: X射线波长
    
    返回:
        结构因子 (复数)
    """
    s = 1.0 / (2.0 * d)
    
    F = 0.0 + 0.0j
    
    for atom in atoms:
        f = atomic_scattering_factor(atom.element, s)
        
        dw = np.exp(-atom.b_iso * s**2)
        
        phase = 2 * np.pi * (h * atom.x + k * atom.y + l * atom.z)
        
        F += atom.occupancy * f * dw * (np.cos(phase) + 1j * np.sin(phase))
    
    return F


def structure_factor_from_positions(h: int, k: int, l: int, 
                                    elements: List[str], 
                                    positions: List[np.ndarray], 
                                    occupancies: List[float],
                                    b_isos: List[float],
                                    d: float) -> complex:
    """
    根据等效位置计算结构因子
    
    参数:
        h, k, l: 衍射指标
        elements: 元素符号列表
        positions: 等效位置列表 (分数坐标)
        occupancies: 占位因子列表
        b_isos: 各向同性温度因子列表
        d: 面间距
    
    返回:
        结构因子 (复数)
    """
    s = 1.0 / (2.0 * d)
    
    F = 0.0 + 0.0j
    
    for elem, pos, occ, b_iso in zip(elements, positions, occupancies, b_isos):
        f = atomic_scattering_factor(elem, s)
        
        dw = np.exp(-b_iso * s**2)
        
        phase = 2 * np.pi * (h * pos[0] + k * pos[1] + l * pos[2])
        
        F += occ * f * dw * (np.cos(phase) + 1j * np.sin(phase))
    
    return F
